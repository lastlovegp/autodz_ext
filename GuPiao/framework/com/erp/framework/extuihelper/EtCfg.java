package com.erp.framework.extuihelper;

/**
 *
 * EXT UI 参数管理
 *
 */
public class EtCfg {
	String beanName = "";
	String beanId = "";

	public String getBeanName() {
		return beanName;
	}

	public void setBeanName(String beanName) {
		this.beanName = beanName;
	}

	public String getBeanId() {
		return beanId;
	}

	public void setBeanId(String beanId) {
		this.beanId = beanId;
	}

}